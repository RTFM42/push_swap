#include "libft.h"

double	ft_atod(const char *a)
{
	long int	i;
	long double	dig;

	i = ft_atoi(a);
	dig = 1;
	if (*a == '-')
	{
		a++;
	}
	while (48 <= *a && *a <= 57)
		a++;
	if (*a == '.' && a++ >= 0x0 && 48 <= *a && *a <= 57)
	{
		while (48 <= *a && *a <= 57)
		{
			i *= 10;
			i += *a - 48;
			dig *= 0.1;
			a++;
		}
	}
	return (dig * i);
}
